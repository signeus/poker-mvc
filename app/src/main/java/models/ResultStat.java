public class ResultStat {
    private int CPR;
    private double CSI;
    private double PN;

    public int getCPR() {
        return CPR;
    }

    public void setCPR(int CPR) {
        this.CPR = CPR;
    }

    public double getCSI() {
        return CSI;
    }

    public void setCSI(double CSI) {
        this.CSI = CSI;
    }

    public double getPN() {
        return PN;
    }

    public void setPN(double PN) {
        this.PN = PN;
    }
}
